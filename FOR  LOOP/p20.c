#include<stdio.h>

int main()
{
	float i,n,j=0.5;
	
	printf("enter n:");
	scanf("%f",&n);

	printf("%f",j);

	for(i=0.5;i<=n;i++)
	{
		j+=i*1;
		printf("\n %f",j);
	}
	
	return 0;
}

